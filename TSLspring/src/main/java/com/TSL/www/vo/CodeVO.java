package com.TSL.www.vo;

public class CodeVO {

	private String cno;
	private int clv;
	private String highc;
	private String cname;
	private String use;
	
	public String getCno() {
		return cno;
	}
	public void setCno(String cno) {
		this.cno = cno;
	}
	public int getClv() {
		return clv;
	}
	public void setClv(int clv) {
		this.clv = clv;
	}
	public String getHighc() {
		return highc;
	}
	public void setHighc(String highc) {
		this.highc = highc;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
	
	
}
